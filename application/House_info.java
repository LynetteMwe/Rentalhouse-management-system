    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
        // TODO add your handling code here:
        DefaultTableModel mdl = (DefaultTableModel)jTable2.getModel();

        int rowIndex = jTable2.getSelectedRow();

        jTextFieldID.setText(mdl.getValueAt(rowIndex,0).toString());
        jComboBoxEstID.setSelectedItem(mdl.getValueAt(rowIndex,1)); 
        jComboBoxLandID.setSelectedItem(mdl.getValueAt(rowIndex,2));
        jComboBoxAgID1.setSelectedItem(mdl.getValueAt(rowIndex,3));
        jTextFieldrent.setText(mdl.getValueAt(rowIndex,4).toString()); 
        jComboBoxSize.setSelectedItem(mdl.getValueAt(rowIndex,5));
        jComboBoxStat.setSelectedItem(mdl.getValueAt(rowIndex,6));

        


    }//GEN-LAST:event_jTable2MouseClicked

