    public void ComboLand(){
              PreparedStatement st;
        ResultSet  rs;
        String querySelection = "SELECT * FROM `landlords`";
        
         try {
             st=cn.createConnection().prepareStatement(querySelection);
              rs = st.executeQuery();
              
              while(rs.next()){
                  String LandlordID=rs.getString("ID");
                  jComboBoxLandID.addItem(LandlordID);
              }
             
         } catch (SQLException ex) {
             Logger.getLogger(Houses.class.getName()).log(Level.SEVERE, null, ex);
         }
            
           
            
        
        
    }
    
