    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String name=jTextFieldperson.getText();
        String amount=String.valueOf(jTextFieldamount.getText());
        String use=jTextFielduse.getText();
        Date date= jDateChooser.getDate();
        String q =k.format(date);
        
        
        if(exp.addExpenses(name, amount, use,q)){
            JOptionPane.showMessageDialog(rootPane, "Successful", "Add_Expenses", JOptionPane.INFORMATION_MESSAGE);
        }
        else{
            JOptionPane.showMessageDialog(rootPane, "Unsuccessful", "Add_Expenses_Error", JOptionPane.ERROR_MESSAGE);

        }
       
    }//GEN-LAST:event_jButton1ActionPerformed

