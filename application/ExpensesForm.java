    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        DefaultTableModel mdl = (DefaultTableModel)jTable1.getModel();
        
        int rowIndex = jTable1.getSelectedRow();
        
        jTextFieldperson.setText(mdl.getValueAt(rowIndex,0).toString());     
        jTextFieldamount.setText(mdl.getValueAt(rowIndex,1).toString());  
        jTextFielduse.setText(mdl.getValueAt(rowIndex,2).toString());
        jTextFielddate.setText(mdl.getValueAt(rowIndex,3).toString());
      
    }//GEN-LAST:event_jTable1MouseClicked

